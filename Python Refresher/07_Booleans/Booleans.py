# Booleans are created by using comparison to calculate whether two thing same or different
print(5 == 5)  # True
print(5 == 6)  # False

print(5 > 5)  # False
print(5 > 4)  # True

print(10 != 10)  # False
print(10 != 11)  # True

# Comparisons: ==, !=, >, <, =>, <=.
print("========== other comparisons ========== ")
friends = {"Bob", "Rolf"}
abroad = {"Bob", "Rolf"}

print(friends == abroad)
print(friends is abroad) # is Checking that two elements exactly the same
